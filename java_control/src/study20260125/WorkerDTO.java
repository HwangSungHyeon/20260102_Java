package study20260125;

public class WorkerDTO {
	
	String name;
	String team;
	String age;
	String phone;
	String work;
	String desiredArea;
    String desiredWork;
	
	public WorkerDTO(String name, String team, String age, String phone, String work, String desiredArea,
			String desiredWork) {
		super();
		this.name = name;
		this.team = team;
		this.age = age;
		this.phone = phone;
		this.work = work;
		this.desiredArea = desiredArea;
		this.desiredWork = desiredWork;
		
	}
	
}
